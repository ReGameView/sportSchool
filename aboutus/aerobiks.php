<h1>Отделение аэробики</h1>
<div class="content"><p style="text-align: center;"><span style="font-size:14px;"><strong>Аэробика&nbsp;— это гармония красоты, эстетики и&nbsp;совершенства. Слово «аэробика» произошло от&nbsp;греческого корня «аэро», имеющего значение «воздух».</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Определение «аэробный» буквально переводится как «живущий на&nbsp;воздухе» или&nbsp;«использующий кислород».</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Аэробика&nbsp;— это комплекс упражнений, в&nbsp;которых дыхательные движения сочетаются с&nbsp;движениями тела, </strong></span><nobr><span style="font-size:14px;"><strong>опорно-двигательного</strong></span></nobr><span style="font-size:14px;"><strong> аппарата.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Аэробика способствует обновлению организма и&nbsp;его функциональному совершенствованию. Быстрый темп требует быстрых движений и&nbsp;изменений положения тела, сочетающихся с&nbsp;завораживающей пластикой.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>В&nbsp;каждом движении чувствуется ритм, скорость и&nbsp;зажигательные телодвижения. В&nbsp;комплексе упражнений используются разнообразные усовершенствованные программы, </strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>направленные на&nbsp;развитие ловкости, гибкости и&nbsp;выносливости.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Занятия аэробикой под&nbsp;специальное музыкальное сопровождение положительно влияют на&nbsp;различные группы мышц, работу нервной и&nbsp;дыхательной системы.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Отделение по&nbsp;аэробике работает с&nbsp;1996&nbsp;года.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Руководит отделением Отличник физической культуры и&nbsp;спорта Наталья Валентиновна Колпакова.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>На&nbsp;отделении занимаются более 200&nbsp;учащихся в&nbsp;группах спортивной аэробики и&nbsp;в&nbsp;группах </strong></span><nobr><span style="font-size:14px;"><strong>фитнес-аэробики.</strong></span></nobr></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Воспитано 2&nbsp;Мастера спорта по&nbsp;спортивной аэробике.</strong></span></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>На&nbsp;отделении работает 8&nbsp;</strong></span><nobr><span style="font-size:14px;"><strong>тренеров-преподавателей.</strong></span></nobr></p>

<p style="text-align: center;"><span style="font-size:14px;"><strong>Из&nbsp;них:</strong></span></p>

